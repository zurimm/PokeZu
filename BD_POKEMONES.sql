CREATE TABLE TIPO_POKEMON(
ID     INT IDENTITY(1,1) PRIMARY KEY,
NOMBRE VARCHAR(100) NOT NULL,
);

SELECT * FROM POKEMON;

CREATE TABLE POKEMON (
  ID                  INT IDENTITY(1,1) PRIMARY KEY,
  NOMBRE              VARCHAR(50) NOT NULL ,
  VIDA                INT  NOT NULL,
  TIPO                INT NOT NULL,
  PUNTAJE_ATAQUE      INT NOT NULL,
  PUNTAJE_DEFENSA     INT NOT NULL,
  PUNTAJE_RESISTENCIA INT NOT NULL,
  CONSTRAINT FK_TIPO FOREIGN KEY (TIPO) REFERENCES TIPO_POKEMON (ID),
);

SELECT * FROM POKEMON;

CREATE TABLE HABILIDADES_POKEMON (
	ID     INT PRIMARY KEY,
	NOMBRE VARCHAR(50) NOT NULL,
	TIPO   INT, -- 0 DEFENSA / 1 ATAQUE
	DESCRIPCION VARCHAR(255) NOT NULL

);

SELECT * FROM HABILIDADES_POKEMON;

CREATE TABLE HABILIDAD_X_POKEMON (
	ID_POKEMON   INT NOT NULL,
	ID_HABILIDAD INT NOT NULL,

	PRIMARY KEY (ID_POKEMON,ID_HABILIDAD),
	CONSTRAINT FK_POKEMON FOREIGN KEY (ID_POKEMON) REFERENCES POKEMON (ID),
	CONSTRAINT FK_HABILIDAD FOREIGN KEY (ID_HABILIDAD) REFERENCES HABILIDADES_POKEMON (ID)
);

